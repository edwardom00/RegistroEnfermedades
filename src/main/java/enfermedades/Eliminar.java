    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        Implementacion enfermedad_dao= new Implementacion();
        Enfermedad enfermedad = new Enfermedad();
        if(cajaTexto_CodigoEliminar.getText().equals("")||cajaTexto_AnioEliminar.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese el codigo del registro a eliminar");
        }else{
            String Codigo =cajaTexto_CodigoEliminar.getText();          
            enfermedad.setCodigo(Codigo);
            enfermedad_dao.eliminar(enfermedad);
            limpiar();
            JOptionPane.showMessageDialog(null, "Registro eliminado");
            }
    }//GEN-LAST:event_botonEliminarActionPerformed

