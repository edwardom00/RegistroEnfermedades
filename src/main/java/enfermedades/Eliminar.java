    private void botonBuscarEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarEliminarActionPerformed
        Implementacion enfermedad_dao= new Implementacion();
        Enfermedad enfermedad = new Enfermedad();
        if(cajaTexto_CodigoEliminar.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite el codigo del registro a buscar");
        }else{
            String codigo =cajaTexto_CodigoEliminar.getText();
            enfermedad.setCodigo(codigo);
            enfermedad_dao.buscar(enfermedad);
            cajaTexto_CodigoEliminar.setText(enfermedad.getCodigo());
            cajaTexto_NombreEliminar.setText(enfermedad.getEnfermedad());
            cajaTexto_CasosEliminar.setText(String.valueOf(enfermedad.getCasos()));
            cajaTexto_AnioEliminar.setText(String.valueOf(enfermedad.getAnio()));
            JOptionPane.showMessageDialog(null, "Registro encontrado");
        }
    }//GEN-LAST:event_botonBuscarEliminarActionPerformed

